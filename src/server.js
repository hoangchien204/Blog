const slugify = (text) =>
  text
    .toLowerCase()
    .normalize('NFD')                     // x√≥a d·∫•u ti·∫øng Vi·ªát
    .replace(/[\u0300-\u036f]/g, '')     // x√≥a c√°c k√Ω t·ª± d·∫•u
    .replace(/[^a-z0-9 ]/g, '')          // b·ªè k√Ω t·ª± ƒë·∫∑c bi·ªát
    .replace(/\s+/g, '-')               
    .replace(/-+/g, '-')                
    .replace(/^-+|-+$/g, '');          

app.get('/api/blogger/:slug', async (req, res) => {
  const { slug } = req.params;

  try {
    const result = await pool.query('SELECT * FROM blogger');
    const post = result.rows.find((row) => slugify(row.title) === slug);

    if (!post) {
      return res.status(404).json({ error: 'Kh√¥ng t√¨m th·∫•y b√†i vi·∫øt' });
    }

    res.json(post);
  } catch (err) {
    console.error('L·ªói khi l·ªçc b√†i vi·∫øt theo slug:', err);
    res.status(500).json({ error: 'L·ªói server' });
  }
});

app.get('/api/photo-albums/:slug', async (req, res) => {
  const { slug } = req.params;

  try {
    const albumResult = await pool.query(
      'SELECT id, title, description, location, date FROM photo_albums'
    );

    const album = albumResult.rows.find((a) => slugify(a.title) === slug);

    if (!album) {
      return res.status(404).json({ error: 'Kh√¥ng t√¨m th·∫•y album' });
    }

    const photoResult = await pool.query(
      'SELECT id, src, alt FROM photos WHERE album_id = $1',
      [album.id]
    );

    res.json({
      id: album.id,
      title: album.title,
      description: album.description,
      location: album.location,
      date: album.date,
      photos: photoResult.rows,
    });
  } catch (err) {
    console.error('L·ªói l·∫•y ·∫£nh theo slug:', err);
    res.status(500).json({ error: 'L·ªói server' });
  }
});


